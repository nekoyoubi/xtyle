import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const siteRoot = resolve(here, "..");
const repoRoot = resolve(siteRoot, "..", "..");

const astroDir = resolve(repoRoot, "packages", "astro", "src");
const svelteDir = resolve(repoRoot, "packages", "svelte", "src");
const elementDir = resolve(repoRoot, "packages", "xtyle", "src", "elements");
const demoDir = resolve(siteRoot, "src", "components", "demos");
const outDir = resolve(siteRoot, "src", "regression");

const kebab = (pascal) =>
	pascal.replace(/([a-z0-9])([A-Z])/g, "$1-$2").toLowerCase();

const pascalNames = (dir, ext) =>
	readdirSync(dir)
		.filter((f) => f.endsWith(ext))
		.map((f) => f.slice(0, -ext.length));

const astro = new Map(pascalNames(astroDir, ".astro").map((p) => [kebab(p), p]));

// The svelte barrel renames some files on export (e.g. Qr.svelte -> QR), so the
// export name is the source of truth, not the filename. Parse it.
const svelteBarrel = readFileSync(resolve(svelteDir, "index.ts"), "utf8");
const svelte = new Map();
for (const m of svelteBarrel.matchAll(
	/export\s*\{\s*default as (\w+)\s*\}\s*from\s*["']\.\/(\w+)\.svelte["']/g,
)) {
	svelte.set(kebab(m[2]), m[1]);
}

const elementIds = new Set(
	readdirSync(elementDir)
		.filter((f) => f.endsWith(".ts") && !f.endsWith(".d.ts"))
		.map((f) => f.slice(0, -3)),
);

const demoIds = readdirSync(demoDir)
	.filter((f) => f.endsWith(".astro"))
	.map((f) => f.slice(0, -".astro".length));

const ids = demoIds
	.filter((id) => astro.has(id) && svelte.has(id) && elementIds.has(id))
	.sort();

mkdirSync(outDir, { recursive: true });

const astroImports = ids
	.map((id) => `import ${astro.get(id)} from "@xtyle/astro/${astro.get(id)}.astro";`)
	.join("\n");
const astroMap = ids.map((id) => `\t"${id}": ${astro.get(id)},`).join("\n");
writeFileSync(
	resolve(outDir, "astro-registry.ts"),
	`// GENERATED by scripts/gen-regression-registries.mjs — do not edit.\n${astroImports}\n\nexport const astroRegistry: Record<string, unknown> = {\n${astroMap}\n};\n`,
);

const svelteNames = ids.map((id) => svelte.get(id));
const svelteImport = `import {\n${svelteNames.map((n) => `\t${n},`).join("\n")}\n} from "@xtyle/svelte";`;
const svelteMap = ids.map((id) => `\t"${id}": ${svelte.get(id)},`).join("\n");
writeFileSync(
	resolve(outDir, "svelte-registry.ts"),
	`// GENERATED by scripts/gen-regression-registries.mjs — do not edit.\n${svelteImport}\n\nexport const svelteRegistry: Record<string, unknown> = {\n${svelteMap}\n};\n`,
);

const elementMap = ids
	.map((id) => `\t"${id}": () => import("@xtyle/core/elements/${id}.js"),`)
	.join("\n");
writeFileSync(
	resolve(outDir, "element-registry.ts"),
	`// GENERATED by scripts/gen-regression-registries.mjs — do not edit.\nexport const elementRegistry: Record<string, () => Promise<unknown>> = {\n${elementMap}\n};\n`,
);

writeFileSync(
	resolve(outDir, "ids.ts"),
	`// GENERATED by scripts/gen-regression-registries.mjs — do not edit.\nexport const REGRESSION_IDS = ${JSON.stringify(ids, null, "\t")} as const;\n`,
);

console.log(`gen-regression-registries: ${ids.length} components wired`);
