import { createRequire } from "node:module";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const require = createRequire(import.meta.url);
const root = dirname(dirname(fileURLToPath(import.meta.url)));
const components = require("prismjs/components.json");

const langs = components.languages;
const canonical = Object.keys(langs)
	.filter((id) => id !== "meta")
	.sort();

const aliases = {};
const requires = {};
for (const id of canonical) {
	const entry = langs[id];
	if (entry.alias) {
		for (const alias of toArray(entry.alias)) aliases[alias] = id;
	}
	const req = toArray(entry.require);
	if (req.length) requires[id] = req;
}

function toArray(value) {
	if (value == null) return [];
	return Array.isArray(value) ? value : [value];
}

const loaderEntries = canonical
	.map((id) => `\t"${id}": () => import("prismjs/components/prism-${id}.js"),`)
	.join("\n");

const aliasEntries = Object.entries(aliases)
	.sort(([a], [b]) => a.localeCompare(b))
	.map(([alias, id]) => `\t"${alias}": "${id}",`)
	.join("\n");

const requireEntries = Object.entries(requires)
	.sort(([a], [b]) => a.localeCompare(b))
	.map(([id, req]) => `\t"${id}": [${req.map((r) => `"${r}"`).join(", ")}],`)
	.join("\n");

const out = `// Generated by scripts/build-code-langs.mjs from prismjs/components.json: do not edit by hand.
// Run \`node scripts/build-code-langs.mjs\` to regenerate after a prismjs upgrade.

export type GrammarLoader = () => Promise<unknown>;

/** Every Prism canonical language id mapped to a code-split \`import()\` of its grammar chunk. */
export const grammarLoaders: Record<string, GrammarLoader> = {
${loaderEntries}
};

/** Prism alias → canonical id (e.g. \`ts\` → \`typescript\`, \`html\` → \`markup\`). */
export const languageAliases: Record<string, string> = {
${aliasEntries}
};

/** Canonical id → the grammar ids it requires loaded first (Prism's \`require\` metadata). */
export const grammarRequires: Record<string, string[]> = {
${requireEntries}
};
`;

const target = join(root, "packages", "xoji", "src", "elements", "code-langs.generated.ts");
writeFileSync(target, out, "utf8");
console.log(`generated ${canonical.length} grammar loaders -> packages/xoji/src/elements/code-langs.generated.ts`);
