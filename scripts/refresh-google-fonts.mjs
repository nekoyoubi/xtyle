#!/usr/bin/env node
/**
 * Regenerates `apps/site/src/lib/google-fonts.generated.ts` — the Google Fonts family list the site installs
 * into the engine, which gives the icon builder its autocomplete, the catalogue's own casing, and an honest
 * "no such font".
 *
 * The list lives in the site, not in `@xtyle/core`, because ~10 KB gzipped of family names is only worth
 * paying where a human is typing one. What the engine ships instead is a character-set gate (`[A-Za-z0-9 ]`
 * plus a length cap), and *that* is what carries the URL-safety property: a family that cannot hold a quote,
 * a paren, a semicolon, an angle bracket, or a newline has nothing to break out of a `url()` or an `href`
 * with.
 *
 * Which makes the check below load-bearing, and more so than when an exact allowlist stood behind it: if
 * Google ever ships a family name outside that character set, this script REFUSES TO WRITE. A silent
 * regeneration that widened the set would rot the engine's whole safety argument without anyone reading a
 * diff. The refusal is the tripwire.
 *
 * Source is `https://fonts.google.com/metadata/fonts`, the endpoint the Google Fonts site itself reads. It
 * needs no API key, unlike the Developer API (`googleapis.com/webfonts/v1`), which is why the list can be
 * refreshed by anyone with a checkout and no secret. The result is vendored as a checked-in TS module so a
 * build never touches the network and a missing key can never break it.
 *
 * Run after a Google Fonts release adds families:
 *
 *   node scripts/refresh-google-fonts.mjs
 *
 * Then review the diff (it should only add names) and commit the regenerated file.
 */
import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

const METADATA_URL = "https://fonts.google.com/metadata/fonts";
const OUT = fileURLToPath(new URL("../apps/site/src/lib/google-fonts.generated.ts", import.meta.url));

/** Every Google family name is `[A-Za-z0-9 ]` today; anything else is a catalogue change we must see. */
const SAFE_FAMILY = /^[A-Za-z0-9]+(?: [A-Za-z0-9]+)*$/;

/** The engine's own cap. A family longer than this cannot pass its gate, so it could never be used. */
const MAX_FAMILY_LENGTH = 64;

const response = await fetch(METADATA_URL, { headers: { accept: "application/json" } });
if (!response.ok) {
	console.error(`google fonts metadata fetch failed: ${response.status} ${response.statusText}`);
	process.exit(1);
}

// The endpoint may prefix the JSON with an anti-hijacking guard.
const metadata = JSON.parse((await response.text()).replace(/^\)\]\}'\n?/, ""));
const families = [...new Set(metadata.familyMetadataList.map((f) => f.family))].sort((a, b) => a.localeCompare(b, "en"));

if (!families.length) {
	console.error("google fonts metadata returned no families; refusing to write an empty list");
	process.exit(1);
}

const unsafe = families.filter((f) => !SAFE_FAMILY.test(f));
if (unsafe.length) {
	console.error(
		`google fonts metadata returned ${unsafe.length} name(s) outside the safe character set, which the engine's URL-safety proof depends on:\n  ${unsafe.map((f) => JSON.stringify(f)).join("\n  ")}\n` +
			"widen the gate in packages/xtyle/src/fonts/google.ts (and re-prove URL safety) before accepting these.",
	);
	process.exit(1);
}

const overlong = families.filter((f) => f.length > MAX_FAMILY_LENGTH);
if (overlong.length) {
	console.error(
		`google fonts metadata returned ${overlong.length} name(s) longer than the engine's ${MAX_FAMILY_LENGTH}-character cap, which would be gated out and never resolve:\n  ${overlong.map((f) => JSON.stringify(f)).join("\n  ")}\n` +
			"raise MAX_FONT_FAMILY_LENGTH in packages/xtyle/src/fonts/google.ts before accepting these.",
	);
	process.exit(1);
}

const source = `/**
 * The Google Fonts family list. Generated by \`scripts/refresh-google-fonts.mjs\` from
 * \`${METADATA_URL}\` — do not edit by hand.
 *
 * Every name is \`[A-Za-z0-9 ]\`; the script refuses to write if Google ever ships one that is not.
 */

const PACKED =
	${JSON.stringify(families.join("\n"))};

/** Every family name Google Fonts serves, canonically cased, sorted. */
export const GOOGLE_FONT_FAMILIES: readonly string[] = Object.freeze(PACKED.split("\\n"));

/** How many families the list holds. */
export const GOOGLE_FONT_COUNT = ${families.length};
`;

await writeFile(OUT, source, "utf8");
console.log(`wrote ${families.length} families to ${OUT}`);
