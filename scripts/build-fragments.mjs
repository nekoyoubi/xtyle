import { build } from "esbuild";
import { readdirSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const fragmentsDir = join(root, "packages", "xtyle", "src", "elements", "fragments");

if (!existsSync(fragmentsDir)) process.exit(0);

const fills = readdirSync(fragmentsDir, { withFileTypes: true })
	.filter((e) => e.isDirectory() && existsSync(join(fragmentsDir, e.name, "mod.ts")))
	.map((e) => e.name);

for (const id of fills) {
	const here = join(fragmentsDir, id);

	await build({
		entryPoints: [join(here, "mod.ts")],
		outfile: join(here, "mod.js"),
		bundle: true,
		format: "iife",
		platform: "neutral",
		target: "es2020",
		legalComments: "none",
		logLevel: "warning",
	});

	const manifest = JSON.parse(readFileSync(join(here, "mod.manifest.json"), "utf8"));
	const entryScript = typeof manifest.entry === "string" ? manifest.entry : manifest.entry.script;

	// Every fill loads into one shared sandbox context whose export table is keyed by bare
	// name, so two components registering the same handler name (e.g. `toggle`) would clobber
	// each other — last load wins, the rest silently no-op. Namespace every handler export by
	// the fragment id at build time: the manifest ref and the `register` call both get the
	// `<id>__` prefix, so collisions are impossible without authors having to remember.
	const prefix = `${id}__`;
	for (const fillList of Object.values(manifest.fills ?? {})) {
		for (const fill of fillList) {
			for (const handler of fill.handlers ?? []) {
				handler.handler = prefix + handler.handler;
			}
		}
	}

	const sources = { [entryScript]: readFileSync(join(here, entryScript), "utf8") };
	sources[entryScript] = sources[entryScript].replace(
		/xript\.exports\.register\(\s*"([^"]+)"/g,
		(_match, name) => `xript.exports.register("${prefix}${name}"`,
	);
	for (const fillList of Object.values(manifest.fills ?? {})) {
		for (const fill of fillList) {
			if (fill.source && !(fill.source in sources)) {
				sources[fill.source] = readFileSync(join(here, fill.source), "utf8");
			}
		}
	}

	const out =
		"// generated by scripts/build-fragments.mjs: do not edit\n" +
		`export const manifest = ${JSON.stringify(manifest)};\n` +
		`export const fragmentSources: Record<string, string> = ${JSON.stringify(sources)};\n`;
	writeFileSync(join(here, "source.generated.ts"), out);
	console.log(`built ${id} component fill + embedded source bundle`);
}
